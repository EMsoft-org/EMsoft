! Function: eu2ro
!
!> @author Marc De Graef, Carnegie Mellon University
!
!> @brief Euler angles to Rodrigues vector (single precision) [Morawiec, page 40]
!
!> @param e 3 Euler angles in radians (single precision)  
!>  
!
!> @date 8/04/13   MDG 1.0 original
!--------------------------------------------------------------------------
